import { LitElement, html } from 'lit';
import { customElement, property } from 'lit/decorators.js';
import './internal-element';
import styles from './my-element.scss';
import fontFaceStyle from './assets/icon-font/font-face.scss';
import { injectStyleIntoHead } from './services';

/**
 * An example element.
 *
 * @fires count-changed - Indicates when the count changes
 * @slot - This element has a slot
 * @csspart button - The button
 */
@customElement('my-element')
export class MyElement extends LitElement {
  static override styles = styles;

  constructor() {
    super();
    injectStyleIntoHead(fontFaceStyle.toString());
  }

  /**
   * The name to say "Hello" to.
   */
  @property()
  name = 'World';

  /**
   * The number of times the button has been clicked.
   */
  @property({type: Number})
  count = 0;

  override render() {
    return html`
      <h1>${this.sayHello(this.name)}!</h1>
      <button @click=${this._onClick} part="button">
        Click Count: ${this.count}
      </button>
      <span class="icon-arrow_back"></span>
      <slot></slot>
      <internal-element></internal-element>
    `;
  }

  private _onClick() {
    this.count++;
    this.dispatchEvent(new CustomEvent('count-changed'));
  }

  /**
   * Formats a greeting
   * @param name The name to say "Hello" to
   */
  sayHello(name: string): string {
    return `Hello, ${name}`;
  }
}

declare global {
  interface HTMLElementTagNameMap {
    'my-element': MyElement;
  }
}
